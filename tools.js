/* Main Function */

var homePage = location.href;
var pages = new Set();
var urls = [homePage];
var reg = new RegExp('^' + homePage);

async function crawlPages() {

    for (var i = 0; i < urls.length; i++) {
        var url = urls[i];
        await fetch(url).then(res => {
            if (/4.*/.test(res.status)) {                                   //4XX Test
                audit['4XX'].push(url);    
            }
            else if (/5.*/.test(res.status)) {                              //5XX Test
                audit['5XX'].push(url)
            }
            else if (/301/.test(res.status)){                               //301 Test
                audit['301'].push(url)
            }
            else if (/302/.test(res.status)){                               //301 Test
                audit['302'].push(url)
            }
            else {                                                          //200 Test
                audit['200'].push(url)
                return res.text();
            }
        }).catch(()=>{
            audit['failed to fetch'].push(url);
            console.log('failed to fetch');
        }).then(html => {
            var parser = new DOMParser();
            var doc = parser.parseFromString(html, 'text/html');
            var links = doc.querySelectorAll('body a[href]');               //Gather Body Links
            for (var j = 0; j < links.length; j++) {
                var href = links[j].href;
                if (reg.test(href) && !pages.has(href) && !/[\?#]/.test(href)) {
                    urls.push(href);
                    pages.add(href);
                }
            }

            /* h1,h2,h3 Check */
            doc.querySelectorAll('h1').length > 1 ? audit['h1'].push(url) : '';
            doc.querySelectorAll('h2').length > 3 ? audit['h2'].push(url) : '';
            doc.querySelectorAll('h3').length > 6 ? audit['h3'].push(url) : '';

            /* Title. Desc Check */
            var title = doc.querySelector('title');
            var desc = doc.querySelector('meta[name="description"]');
            if(title){
                audit['titles'].push(title.innerText);
                title.innerText.length > 26 ? audit['title too long'].push(url) : ''
            }
            else{
                audit['title unset'].push(url)
            }
            if(desc){
                audit['descs'].push(desc.content);
                desc.content.length > 70 ? audit['desc too long'].push(url) : 
                desc.content.length < 10 ? audit['desc unset'].push(url) : ''
            }
            else{
                audit['desc unset'].push(url)
            }

            /* htmlLang Check */
            doc.querySelector('html[lang]') ? '' : audit['htmlLang'].push(url);

            /* OG Set */
            doc.querySelector('meta[data-hid="og:title"]') ? '' : audit['ogSet'].push(url);

            /* Structured Data && Breadcrumb List */
            var sData = doc.querySelector('script[type$="ld+json"]');
            if (sData) {
                /BreadcrumbList/.test(sData.innerHTML) ? '' : audit['BreadCrumbList'].push(url)
            }
            else {
                audit['StructuredData'].push(url);
            }

            /* Canonical Check */
            var can = doc.querySelector('link[rel="canonical"]');
            can ? '' : audit['canonical unset'].push(url);

            /* Html 5 Tag Check */
            var html5Tags = ['header', 'nav', 'main', 'aside', 'article', 'section', 'footer', 'mark', 'time'];
            for (var i = 0; i < html5Tags.length; i++) {
                doc.querySelector(html5Tags[i]) ? '' : audit[html5Tags[i]].push(url)
            }

            /* Lazyload Check */
            var imgs = doc.querySelector('img[loading="lazy"]');
            imgs ? audit['lazyload'] = true : '';
            console.log('working!')
        })
    }
    console.log('Pages\n' + urls.join('\n'));                                                       //All Pages
    audit['descs'] = audit['descs'].filter(x => x !== '');
    var keys = Object.keys(audit);
    var values = Object.values(audit);
    for(var k=0; k<keys.length; k++){
        if(keys[k] === 'lazyload'){continue}
        console.log(keys[k] + '\n' + values[k].join('\n'))
    }
    console.log('Mission Complete !');
}
crawlPages();


